using System;
class DeclareFiveVariables
{
    static void Main()
    {
        byte a = 97;
        sbyte b = -115;
        short c = -10000;
        ushort d = 52130;
        int e = 4825932;
        Console.WriteLine("byte = {0}; sbyte = {1}; short = {2}; ushort = {3}; int = {4}",a,b,c,d,e);
    }
}

